        System.out.println("2. What is the value of each of the following Boolean expressions? ");

        System.out.println("a. 5 < 8 		True");
        System.out.println("b. 4 <= 9 		True");
        System.out.println("c. 3 == 4 		False");
        System.out.println("d. 12 >= 12 		True");
        System.out.println("e. 3 + 4 == 8 		False");
        System.out.println("f. 7 < 9 – 2 		False");
        System.out.println("g. 5 != 5 		False");
        System.out.println("h. 15 != 3 * 5 		False");
        System.out.println("i. 9 != –9 		True");
        System.out.println("j. 3 + 5 * 2 == 16	False");
